public class Body {
    private int i;
    private int w;
    private int p;

    public Body(int i, int w, int p) {
        this.i = i;
        this.w = w;
        this.p = p;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }

    public int getW() {
        return w;
    }

    public void setW(int w) {
        this.w = w;
    }

    public int getP() {
        return p;
    }

    public void setP(int p) {
        this.p = p;
    }
}
